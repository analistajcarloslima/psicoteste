    private void btPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPesquisarActionPerformed
        List<TanqueRede> lista;
        TanqueRedeDAO dao = new TanqueRedeDAO();
        lista = (dao.listar());
        TanqueRedeTableModel itm = new TanqueRedeTableModel(lista);
        Object o = TelaConsultaTanqueRede.exibeTela(itm, "TanqueRede");
        if (o != null) {
            tanqueRede = tanqueRedeRN.pesquisarCodigo(Short.valueOf(String.valueOf(o)));
            tfArea.setText(String.valueOf(tanqueRede.getAreaTanqueRede()));
            tfNomeCompleto.setText(tanqueRede.getNomeTanqueRede());
            tfMaxAlevinos.setText(String.valueOf(tanqueRede.getLimiteAlevinosTanqueRede()));
            if(tanqueRede.isSituacaoTanqueRede()==false){
                tfMaxAlevinos.setEnabled(false);
            }
            EstadoPeixe.setSelectedItem(tanqueRede.getEstadoPeixe());
            btExcluir3.setEnabled(true);

        }
    }//GEN-LAST:event_btPesquisarActionPerformed


    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        if (tanqueRede == null) {
            tanqueRede = new TanqueRede();
        }
        if (tanqueRede.getIdTanqueRede() == 0) {
            tanqueRede.setNomeTanqueRede(tfNomeCompleto.getText());
            tanqueRede.setEstadoPeixe(EstadoPeixe.getSelectedItem().toString());
            tanqueRede.setLimiteAlevinosTanqueRede(Integer.parseInt(tfMaxAlevinos.getText()));
            tanqueRede.setAreaTanqueRede(Double.parseDouble(tfArea.getText().toString()));
            tanqueRedeDAO.salvar(tanqueRede);
            JOptionPane.showMessageDialog(rootPane, "Tanque Rede Cadastrado com Sucesso!");
            limparCampos();
        } else {
            tanqueRede.setNomeTanqueRede(tfNomeCompleto.getText());
            tanqueRede.setEstadoPeixe(EstadoPeixe.getSelectedItem().toString());
            tanqueRede.setLimiteAlevinosTanqueRede(Integer.parseInt(tfMaxAlevinos.getText()));
            tanqueRede.setAreaTanqueRede(Double.parseDouble(tfArea.getText().toString()));
            tanqueRedeDAO.salvar(tanqueRede);
            if (JOptionPane.showConfirmDialog(rootPane, "Deseja realmente editar o Tanque Rede " + tanqueRede.getNomeTanqueRede()
                    + "?", "TechPesc", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE) == JOptionPane.YES_OPTION) {
                tanqueRedeDAO.salvar(tanqueRede);
                JOptionPane.showMessageDialog(rootPane, "Pronto,  " + tanqueRede.getNomeTanqueRede()
                        + ", foi editado com sucesso!", "Tanque Rede", JOptionPane.INFORMATION_MESSAGE);
            }
            limparCampos();
        }
    }//GEN-LAST:event_btSalvarActionPerformed

    private void btExcluir3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluir3ActionPerformed
        if (tanqueRede != null) {
            if (tanqueRede.getIdTanqueRede() != 0) {
                if (tanqueRede.isSituacaoTanqueRede() == true) {
                    if (JOptionPane.showConfirmDialog(rootPane, "Deseja excluir o Tanque Rede " + tanqueRede.getNomeTanqueRede()
                            + "?", "TechPesc", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE) == JOptionPane.YES_OPTION) {
                        if (tanqueRedeRN.remove(tanqueRede)) {
                            JOptionPane.showMessageDialog(rootPane, "Pronto,  " + tanqueRede.getNomeTanqueRede()
                                    + ", foi excluído com sucesso!", "Tanque Rede", JOptionPane.INFORMATION_MESSAGE);
                        } else {
                            JOptionPane.showMessageDialog(rootPane, "Não foi possível excluir o Tanque Rede"
                                    + tanqueRede.getNomeTanqueRede(),
                                    "Erro ao Excluir", JOptionPane.ERROR_MESSAGE);
                        }
                    }

                } else {
                    JOptionPane.showMessageDialog(rootPane, "Não é possível excluir um tanque rede que está vinculado a algum lote!",
                            "Erro ao Excluir", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
        limparCampos();
    }//GEN-LAST:event_btExcluir3ActionPerformed

    public void limparCampos() {
        tfArea.setText("");
        tfNomeCompleto.setText("");
        tfMaxAlevinos.setText("");
        EstadoPeixe.setSelectedItem("----");
        tfMaxAlevinos.setEnabled(true);
        btExcluir3.setEnabled(false);
        tanqueRede = null;
    }

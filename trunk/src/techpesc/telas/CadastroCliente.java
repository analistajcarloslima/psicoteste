    private void btExcluir3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluir3ActionPerformed
        if (cliente != null) {
            if (cliente.getIdCliente() != 0) {

                if (JOptionPane.showConfirmDialog(rootPane, "Deseja excluir o Cliente " + cliente.getNomeCliente()
                        + "?", "TechPesc", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE) == JOptionPane.YES_OPTION) {
                    if (clienteRN.remove(cliente)) {
                        JOptionPane.showMessageDialog(rootPane, "Pronto,  O Cliente" + cliente.getNomeCliente()
                                + ", foi excluído com sucesso!", "Cliente", JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Não foi possível excluir o Cliente " + cliente.getNomeCliente(),
                                "Erro ao Excluir", JOptionPane.ERROR_MESSAGE);
                    }
                }

            }
        }
        limparCampos();

    }//GEN-LAST:event_btExcluir3ActionPerformed

